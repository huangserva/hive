export type I18nKey = keyof typeof en
export type TFunction = (key: I18nKey, params?: Record<string, string | number | null>) => string

const catalogs = { en, zh }

const detectLanguage = (): AppLanguage => {
  const locale = Localization.getLocales()[0]?.languageTag?.toLowerCase() ?? ''
  return locale.startsWith('zh') ? 'zh' : 'en'
}

const readStoredLanguage = async (): Promise<AppLanguage | null> => {
  try {
    const stored = await SecureStore.getItemAsync(LANGUAGE_KEY)
    return stored === 'en' || stored === 'zh' ? stored : null
  } catch {
    return null
  }
}

const writeStoredLanguage = async (language: AppLanguage) => {
  try {
    await SecureStore.setItemAsync(LANGUAGE_KEY, language)
  } catch {
    // SecureStore can be unavailable in simulator/web paths; context state still updates.
  }
}

const interpolate = (template: string, params?: Record<string, string | number | null>) =>
  template.replace(/\{(\w+)\}/gu, (_, key: string) => String(params?.[key] ?? ''))

interface LanguageContextValue {
  language: AppLanguage
  setLanguage: (language: AppLanguage) => Promise<void>
  t: TFunction
}

const LanguageContext = createContext<LanguageContextValue | null>(null)

export const LanguageProvider = ({ children }: PropsWithChildren) => {
  const [language, setLanguageState] = useState<AppLanguage>(() => detectLanguage())

  useEffect(() => {
    let mounted = true
    void readStoredLanguage().then((stored) => {
      if (mounted && stored) setLanguageState(stored)
    })
    return () => {
      mounted = false
    }
  }, [])

  const setLanguage = useCallback(async (nextLanguage: AppLanguage) => {
    setLanguageState(nextLanguage)
    await writeStoredLanguage(nextLanguage)
  }, [])

  const value = useMemo<LanguageContextValue>(
    () => ({
      language,
      setLanguage,
      t: (key, params) => interpolate(catalogs[language][key] ?? catalogs.en[key], params),
    }),
    [language, setLanguage]
  )

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export const useLanguage = () => {
  const context = useContext(LanguageContext)
  if (!context) throw new Error('useLanguage must be used within LanguageProvider')
  return context
}

export const useT = () => useLanguage().t
